function openModal(title, bodyHtml, footerHtml = '') {
  document.getElementById('modal-title').textContent = title;
  document.getElementById('modal-body').innerHTML = bodyHtml;
  document.getElementById('modal-footer').innerHTML = footerHtml;
  document.getElementById('modal-overlay').classList.remove('hidden');
}

function closeModal() {
  document.getElementById('modal-overlay').classList.add('hidden');
}

function formatDate(dateStr) {
  if (!dateStr) return '—';
  try { return new Date(dateStr).toLocaleDateString('en-IN', { day:'2-digit', month:'short', year:'numeric' }); }
  catch { return dateStr; }
}

function formatDateTime(dateStr) {
  if (!dateStr) return '—';
  try { return new Date(dateStr).toLocaleString('en-IN', { day:'2-digit', month:'short', year:'numeric', hour:'2-digit', minute:'2-digit' }); }
  catch { return dateStr; }
}

function formatDuration(seconds) {
  const m = Math.floor(seconds / 60);
  const s = seconds % 60;
  return `${m}m ${s < 10 ? '0' : ''}${s}s`;
}

function pct(score, total) {
  return total > 0 ? ((score / total) * 100).toFixed(1) : '0.0';
}

function badgeStatus(status) {
  const map = { active:'badge-active', draft:'badge-draft', inactive:'badge-inactive' };
  return `<span class="badge ${map[status] || 'badge-inactive'}">${status}</span>`;
}

function badgeRole(role) {
  return `<span class="badge ${role === 'admin' ? 'badge-admin' : 'badge-student'}">${role}</span>`;
}

function badgeResult(passed) {
  return passed
    ? `<span class="badge badge-pass">✓ Pass</span>`
    : `<span class="badge badge-fail">✗ Fail</span>`;
}

function confirmAction(message, onConfirm) {
  openModal('Confirm Action',
    `<p style="color:var(--text-2)">${message}</p>`,
    `<button class="btn-secondary" onclick="closeModal()">Cancel</button>
     <button class="btn-danger" onclick="closeModal();(${onConfirm.toString()})()">Confirm</button>`
  );
}

